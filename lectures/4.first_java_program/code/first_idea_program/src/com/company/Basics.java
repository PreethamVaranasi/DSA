package com.company;

public class Basics {
    public static void main(String[] args) {
        // if statement
        if(true){
            System.out.println("Hello world");
        }
        int c = 1;
        // while loop
        while(c != 5){
            System.out.println(c);
            c++;
        }
        // for loop
        for (int count = 1; count <= 5; count++){
            System.out.println(count);
        }
    }
}
